
// Mile To Kilometer
let mileToKilometer = 1.60934;

// Taking input from user to input miles
let miles = prompt("Enter distance in miles");

// Formula to calculate how many kilometers in miles
let kilometers = miles * mileToKilometer;

// Declaring variable to show the distance in miles to kilometers and putting values in it
let sentence = "The Distance of " + miles + " miles is equal to " + kilometers + " kms."
console.log(sentence);