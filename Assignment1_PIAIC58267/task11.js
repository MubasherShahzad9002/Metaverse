// Temperature in celsius to fahrenheit
var celsius = 100;
var fahrenheit = celsius * 1.8 + 32;
document.write(`${celsius} degree celsius is equal to ${fahrenheit} degree fahrenheit.` + "<br></br>");

// Temperature in fahrenheit to celcius
var fahrenheit = 212;
var celsius = (fahrenheit - 32) / 1.8;
document.write(`${fahrenheit} degree celsius is equal to ${celsius} degree fahrenheit.`);

